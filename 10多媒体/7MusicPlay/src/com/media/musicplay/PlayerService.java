package com.media.musicplay;

import java.util.Timer;
import java.util.TimerTask;

import android.app.Service;
import android.content.Intent;
import android.media.MediaPlayer;
import android.media.MediaPlayer.OnPreparedListener;
import android.os.Binder;
import android.os.Bundle;
import android.os.IBinder;
import android.os.Message;

public class PlayerService extends Service {

	private MediaPlayer player;
	private Timer timer;
	@Override
	public IBinder onBind(Intent intent) {
		return new ZhongJianRenPlayer();
	}

	
	@Override
	public void onCreate() {
		super.onCreate();
		player = new MediaPlayer();
	}


	@Override
	public void onDestroy() {
		super.onDestroy();
		//停止播放
		player.stop();
		//释放占用的资源，此时player对象已经废掉了
		player.release();
		player = null;
		if(timer != null){
			timer.cancel();
			timer = null;
		}
	}


	public void play() {
		//重置
		player.reset();
		try {
			//加载多媒体文件
//			player.setDataSource(getFilesDir() + "qq.mp3");
			player.setDataSource("http://192.168.1.102:8080/qq.mp3");
			//异步加载,即开个子线程加载，因为网速慢，所以用异步
			player.prepareAsync();

			//同步加载，用在本地加载,本地加载也可以用异步加载
//			player.prepare();
//					player.start();
			player.setOnPreparedListener(new OnPreparedListener() {
				//准备完毕时，此方法调用
				@Override
				public void onPrepared(MediaPlayer mp) {
					player.start();
					addTimer();
				}
			});
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} 
	}
	public void pause() {
		player.pause();
	}
	//继续播放
	public void goOnPlay() {
		player.start();
	}
	public void seekTo(int progress) {
		player.seekTo(progress);
	}
	
	public void addTimer(){
		if(timer == null){
			timer = new Timer();
			timer.schedule(new TimerTask() {
				
				@Override
				public void run() {
					//获取歌曲总时长
					int duration = player.getDuration();
					//获取歌曲当前播放进度
					int currentPosition= player.getCurrentPosition();
					
					Message msg = MainActivity.handler.obtainMessage();
					//把进度封装至消息对象中
					Bundle bundle = new Bundle();
					bundle.putInt("duration", duration);
					bundle.putInt("currentPosition", currentPosition);
					msg.setData(bundle);
					MainActivity.handler.sendMessage(msg);
					
				}
				//开始计时任务后的5毫秒，第一次执行run方法，以后每500毫秒执行一次
			}, 5, 500);
		}
	}
	
	class ZhongJianRenPlayer extends Binder implements IPlay {

		@Override
		public void play() {
			PlayerService.this.play();
		}

		@Override
		public void pause() {
			PlayerService.this.pause();
			
		}

		@Override
		public void goOnPlay() {
			
			PlayerService.this.goOnPlay();
		}

		@Override
		public void seekTo(int progress) {
			PlayerService.this.seekTo(progress);
			
		}
		
	}
}
